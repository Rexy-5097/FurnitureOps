'use client';

import { useState } from 'react';
import { createBrowserClient } from '@/lib/supabase-browser';
import { Lock, Mail, Loader2, ArrowRight } from 'lucide-react';
import GlassCard from '@/components/ui/GlassCard';
import Link from 'next/link';

/* ── Google Logo SVG ── */
function GoogleIcon({ className }: { className?: string }) {
  return (
    <svg className={className} viewBox="0 0 24 24" fill="none">
      <path d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92a5.06 5.06 0 0 1-2.2 3.32v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.1z" fill="#4285F4" />
      <path d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" fill="#34A853" />
      <path d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18A10.96 10.96 0 0 0 1 12c0 1.77.43 3.45 1.18 4.93l3.66-2.84z" fill="#FBBC05" />
      <path d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" fill="#EA4335" />
    </svg>
  );
}

export default function LoginPage() {
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [loading, setLoading] = useState(false);
  const [googleLoading, setGoogleLoading] = useState(false);
  const [error, setError] = useState<string | null>(null);

  /* ── Google OAuth ── */
  const handleGoogleLogin = async () => {
    setGoogleLoading(true);
    setError(null);

    const supabase = createBrowserClient();
    const { error: authError } = await supabase.auth.signInWithOAuth({
      provider: 'google',
      options: {
        // Redirect straight to dashboard — the Supabase client's
        // detectSessionInUrl: true will auto-exchange the PKCE code
        redirectTo: `${window.location.origin}/dashboard`,
      },
    });

    if (authError) {
      setError(authError.message);
      setGoogleLoading(false);
    }
  };

  /* ── Email + Password ── */
  const handleEmailLogin = async (e: React.FormEvent) => {
    e.preventDefault();
    setLoading(true);
    setError(null);

    const supabase = createBrowserClient();
    const { error: authError } = await supabase.auth.signInWithPassword({
      email,
      password,
    });

    if (authError) {
      setError(authError.message);
      setLoading(false);
    } else {
      window.location.href = '/dashboard';
    }
  };

  const isAnyLoading = loading || googleLoading;

  return (
    <div className="flex min-h-screen flex-col items-center justify-center p-6 relative overflow-hidden">
      {/* Animated Background */}
      <div className="absolute inset-0 bg-black" />

      {/* Drifting orbs */}
      <div className="absolute top-[-20%] left-[-10%] w-[50%] h-[50%] bg-blue-600/20 blur-[120px] rounded-full mix-blend-screen animate-drift" />
      <div
        className="absolute bottom-[-20%] right-[-10%] w-[50%] h-[50%] bg-purple-600/20 blur-[120px] rounded-full mix-blend-screen animate-drift"
        style={{ animationDelay: '3s' }}
      />
      <div
        className="absolute top-[30%] left-[60%] w-[35%] h-[35%] bg-indigo-500/10 blur-[100px] rounded-full mix-blend-screen animate-drift"
        style={{ animationDelay: '5s', animationDuration: '10s' }}
      />

      {/* Floating particles */}
      <div className="absolute inset-0 pointer-events-none">
        {[
          { size: 3, top: '20%', left: '15%', delay: '0s' },
          { size: 2, top: '70%', left: '80%', delay: '1s' },
          { size: 4, top: '40%', left: '70%', delay: '2s' },
          { size: 2, top: '80%', left: '25%', delay: '1.5s' },
        ].map((p, i) => (
          <div
            key={i}
            className="particle animate-float"
            style={{ width: p.size, height: p.size, top: p.top, left: p.left, animationDelay: p.delay, opacity: 0.3 }}
          />
        ))}
      </div>

      {/* Login Card */}
      <GlassCard className="w-full max-w-md p-8 relative z-10 border-white/10 bg-black/40 shadow-2xl backdrop-blur-2xl animate-fade-in-blur">
        <div>
          {/* Header */}
          <div className="text-center space-y-2 mb-7 animate-fade-up">
            <h1 className="text-3xl font-bold tracking-tight text-white">FurnitureOps</h1>
            <p className="text-sm text-white/50">Enterprise Access Portal</p>
          </div>

          {/* Google Sign-In */}
          <div className="animate-fade-up" style={{ animationDelay: '0.1s' }}>
            <button
              type="button"
              onClick={handleGoogleLogin}
              disabled={isAnyLoading}
              className="w-full flex items-center justify-center gap-3 py-3.5 bg-white text-gray-800 rounded-xl font-semibold hover:bg-gray-100 focus:ring-4 focus:ring-white/30 transition-all duration-300 disabled:opacity-70 disabled:cursor-not-allowed active:scale-[0.98] shadow-lg"
            >
              {googleLoading ? (
                <Loader2 className="h-5 w-5 animate-spin text-gray-600" />
              ) : (
                <>
                  <GoogleIcon className="h-5 w-5" />
                  Sign in with Google
                </>
              )}
            </button>
          </div>

          {/* Divider */}
          <div className="flex items-center gap-4 my-6 animate-fade-up" style={{ animationDelay: '0.15s' }}>
            <div className="flex-1 h-px bg-white/10" />
            <span className="text-xs text-white/30 uppercase tracking-widest">or continue with email</span>
            <div className="flex-1 h-px bg-white/10" />
          </div>

          {/* Email + Password Form */}
          <form onSubmit={handleEmailLogin} className="space-y-4">
            {/* Email */}
            <div className="space-y-2 animate-fade-up" style={{ animationDelay: '0.2s' }}>
              <label className="text-xs font-medium text-white/70 uppercase tracking-widest pl-1" htmlFor="email">
                Email Address
              </label>
              <div className="relative group">
                <Mail className="absolute left-3 top-3 h-5 w-5 text-white/40 group-focus-within:text-blue-400 transition-colors duration-300" />
                <input
                  id="email"
                  type="email"
                  placeholder="admin@furnitureops.com"
                  required
                  value={email}
                  onChange={(e) => setEmail(e.target.value)}
                  disabled={isAnyLoading}
                  className="w-full pl-10 pr-4 py-3 bg-white/5 border border-white/10 rounded-xl text-white placeholder:text-white/20 focus:ring-2 focus:ring-blue-500/50 focus:border-blue-500/50 outline-none transition-all duration-300 disabled:opacity-50"
                />
              </div>
            </div>

            {/* Password */}
            <div className="space-y-2 animate-fade-up" style={{ animationDelay: '0.25s' }}>
              <label className="text-xs font-medium text-white/70 uppercase tracking-widest pl-1" htmlFor="password">
                Password
              </label>
              <div className="relative group">
                <Lock className="absolute left-3 top-3 h-5 w-5 text-white/40 group-focus-within:text-purple-400 transition-colors duration-300" />
                <input
                  id="password"
                  type="password"
                  placeholder="••••••••"
                  required
                  value={password}
                  onChange={(e) => setPassword(e.target.value)}
                  disabled={isAnyLoading}
                  className="w-full pl-10 pr-4 py-3 bg-white/5 border border-white/10 rounded-xl text-white placeholder:text-white/20 focus:ring-2 focus:ring-purple-500/50 focus:border-purple-500/50 outline-none transition-all duration-300 disabled:opacity-50"
                />
              </div>
            </div>

            {/* Submit */}
            <div className="animate-fade-up" style={{ animationDelay: '0.3s' }}>
              <button
                type="submit"
                disabled={isAnyLoading}
                className="w-full flex items-center justify-center py-3.5 bg-gradient-to-r from-blue-600 to-purple-600 text-white rounded-xl font-bold hover:shadow-lg hover:shadow-blue-600/25 focus:ring-4 focus:ring-blue-500/30 transition-all duration-300 disabled:opacity-70 disabled:cursor-not-allowed active:scale-[0.98] bg-[length:200%_100%] hover:bg-right"
              >
                {loading ? (
                  <Loader2 className="h-5 w-5 animate-spin" />
                ) : (
                  <span className="flex items-center gap-2">
                    Sign In <ArrowRight className="h-4 w-4" />
                  </span>
                )}
              </button>
            </div>
          </form>

          {/* Error Message */}
          {error && (
            <div className="mt-4 animate-fade-up p-3 text-sm text-red-200 bg-red-500/20 border border-red-500/30 rounded-lg">
              {error}
            </div>
          )}

          {/* Back to Landing */}
          <div className="mt-6 text-center animate-fade-up" style={{ animationDelay: '0.4s' }}>
            <Link href="/" className="text-xs text-white/30 hover:text-white transition-colors duration-300">
              Back to Landing
            </Link>
          </div>
        </div>
      </GlassCard>
    </div>
  );
}
